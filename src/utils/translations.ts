export const t = (key: string, language = "en"): string => {
  const lang = translations[language as keyof typeof translations] || translations.en
  const translation = lang[key as keyof typeof lang]

  // If translation exists, return it
  if (translation) return translation

  // If no translation found, try English as fallback
  if (language !== "en") {
    const englishTranslation = translations.en[key as keyof typeof translations.en]
    if (englishTranslation) return englishTranslation
  }

  // If still no translation, return the key itself
  return key
}